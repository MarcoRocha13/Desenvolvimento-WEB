//funções sem retorno
function somar(n1,n2)
{
    var r 
    r=n1+n2
    alert("A função de soma sem retorno! O resultado é "+r)
}
somar(10,5)
function subtrair(n1,n2)
{
    var r
    r=n1-n2
    return(r)
}
var resultado
resultado=subtrair(10,5)
alert("A função de subtrair com retorno! O resultado é "+resultado)
